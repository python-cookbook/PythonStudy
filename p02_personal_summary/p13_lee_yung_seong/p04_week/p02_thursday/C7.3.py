#함수 인자에 메터 데이터 넣기
#문제
#함수를 작성했다 이 때 인자에 정보를 추가해서 다른 사람이 함수를 어떻게 사용해야 하는지 알 수 있도록 하고 싶다.
#해결
#함수 인자 주석으로 프로그래머에게 이 함수를 어떻게 사용해야 할지 정보를 줄 수 있다.
def add(x:int, y:int) -> int:
    return x + y
#파이썬 인터프리터는 주석에 어떠한 의미도 부여하지 않는다. 타입을 확인하지도 않고 파이썬의 실행 방식이 달라지지도 않는다.
#단지 소스코드를 읽는 사람이 이해하기 쉽도록 설명 할 뿐.
#토론
#함수 주석은 함수의 __annotations__속성에 저장된다.
add.__annotations__
#주석을 활용할 수 있는 방법은 많지만 기본적으로는 문서화에 도움을 주기위해 사용한다.